package com.moalfarras.moplayer.data.db

import com.moalfarras.moplayer.domain.model.Category
import com.moalfarras.moplayer.domain.model.MediaItem
import com.moalfarras.moplayer.domain.model.ServerProfile

fun ServerEntity.toDomain() = ServerProfile(
    id = id,
    name = name,
    kind = kind,
    baseUrl = baseUrl,
    username = username,
    password = password,
    playlistUrl = playlistUrl,
    createdAt = createdAt,
    lastSyncAt = lastSyncAt,
    host = host,
    accountStatus = accountStatus,
    expiryDate = expiryDate,
    activeConnections = activeConnections,
    maxConnections = maxConnections,
    allowedOutputFormats = allowedOutputFormats.split(',').map(String::trim).filter(String::isNotBlank),
    timezone = timezone,
    serverMessage = serverMessage,
    lastSyncSource = lastSyncSource,
    epgUrl = epgUrl,
    sourceKey = sourceKey,
)

fun ServerProfile.toEntity() = ServerEntity(
    id = id,
    name = name,
    kind = kind,
    baseUrl = baseUrl,
    username = username,
    password = password,
    playlistUrl = playlistUrl,
    createdAt = createdAt,
    lastSyncAt = lastSyncAt,
    host = host,
    accountStatus = accountStatus,
    expiryDate = expiryDate,
    activeConnections = activeConnections,
    maxConnections = maxConnections,
    allowedOutputFormats = allowedOutputFormats.joinToString(","),
    timezone = timezone,
    serverMessage = serverMessage,
    lastSyncSource = lastSyncSource,
    epgUrl = epgUrl,
    sourceKey = sourceKey,
)

fun CategoryEntity.toDomain() = Category(id, serverId, type, name, sortOrder, parentId, rawJson)

fun Category.toEntity() = CategoryEntity(id, serverId, type, name, sortOrder, parentId, rawJson)

